const activeGlossaryEntries = glossaryEntries.filter((entry) => entry.patterns.length > 0);

const glossaryPattern = new RegExp(
  `(?<![\\p{L}\\p{N}_])(?:${activeGlossaryEntries
    .map((entry) => `(?<${entry.id}>${entry.patterns.join("|")})`)
    .join("|")})(?![\\p{L}\\p{N}_])`,
  "giu",
);

function entryForMatch(groups?: Record<string, string | undefined>) {
  const id = Object.entries(groups ?? {}).find(([, value]) => value)?.[0];
  return id ? activeGlossaryEntries.find((entry) => entry.id === id) : undefined;
}

export function TermText({
  children,
  limit = 8,
  scope,
}: {
  children: string;
  limit?: number;
  scope?: Set<string>;
}) {
  if (!children) return null;

  const nodes: ReactNode[] = [];
  const seen = new Set<string>();
  let lastIndex = 0;

  for (const match of children.matchAll(glossaryPattern)) {
    const index = match.index ?? 0;
    const term = match[0];
    const entry = entryForMatch(match.groups);

    if (!entry || seen.size >= limit || seen.has(entry.id) || scope?.has(entry.id)) continue;

    if (index > lastIndex) nodes.push(children.slice(lastIndex, index));

    nodes.push(
      <dfn
        aria-label={`${term}: ${entry.definition}`}
        className="glossary-term"
        data-definition={entry.definition}
        data-term={entry.label}
        key={`${entry.id}-${index}`}
        tabIndex={0}
        title={entry.definition}
      >
        {term}
      </dfn>,
    );

    seen.add(entry.id);
    scope?.add(entry.id);
    lastIndex = index + term.length;
  }

  if (!nodes.length) return <>{children}</>;
  if (lastIndex < children.length) nodes.push(children.slice(lastIndex));

  return (
    <>
      {nodes.map((node, index) => (
        <Fragment key={index}>{node}</Fragment>
      ))}
    </>
  );
}
