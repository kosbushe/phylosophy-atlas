export const philosophers: Philosopher[] = [
  ...featuredPhilosophers,
  ...expandedPhilosophers,
]
  .map((philosopher) => {
    const canonicalFigure = canonFigures.find((figure) => figure.id === philosopher.slug);
    const examples =
      ideaExamples[philosopher.slug] ??
      philosopher.ideas.map((idea) => idea.example);

    if (!canonicalFigure) {
      throw new Error(
        `Нарушена целостность данных: ${philosopher.slug} отсутствует в каноне.`,
      );
    }

    if (
      !examples ||
      examples.length !== philosopher.ideas.length ||
      examples.some((example) => !example?.trim())
    ) {
      throw new Error(
        `Нарушена целостность данных: ${philosopher.slug} должен иметь ${philosopher.ideas.length} современных примеров.`,
      );
    }

    return normalizeProfileTypography(applyProfileDefaults({
      ...philosopher,
      index: canonicalFigure.index,
      ideas: philosopher.ideas.map((idea, index) => ({
        ...idea,
        example: examples[index]!,
      })),
    }));
  })
  .sort((left, right) => Number(left.index) - Number(right.index));

if (
  philosophers.length !== 100 ||
  new Set(philosophers.map((philosopher) => philosopher.slug)).size !== 100
) {
  throw new Error("Редакционный корпус должен содержать ровно 100 уникальных профилей.");
}

export function getPhilosopher(slug: string) {
  return philosophers.find((philosopher) => philosopher.slug === slug)!;
}
